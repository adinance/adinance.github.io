function NavMenu_display(setMenu,obj)
{
	NavMenu_destroy();
	clearTimeout(timerID);

	Menu = document.getElementById("SCGMenu"+setMenu);
	leftPos = findPosX(obj);
	topPos = obj.height;

	if (setMenu == "1") {
		leftPos	-=	9;
	}
	else if (setMenu == "8") {
		leftPos	-=	96;
	}
	else {
		leftPos	-=	10;
	}

	Menu.style.left = leftPos+'px';
	//Menu.style.top = topPos+'px';
	Menu.style.display = '';

	objTdMenu = obj;
	strCurrentMenuId = setMenu;

	objTdMenu.className = "text_Menu_Over";
	if (document.getElementById("SCGMainImageMenu"+strCurrentMenuId)) {
		document.getElementById("SCGMainImageMenu"+strCurrentMenuId).src = "/js/images/sp_menu01_a.gif";	
		document.getElementById("SCGMainImageMenu"+strCurrentMenuId+"R").src = "/js/images/sp_menu02_a.gif";	
	}
}

function NavMenu_destroy()
{
	if(Menu) Menu.style.display = 'none';

	objTdMenu.className = "text_Menu";
	if (document.getElementById("SCGMainImageMenu"+strCurrentMenuId)) {
		document.getElementById("SCGMainImageMenu"+strCurrentMenuId).src = "/js/images/sp_menu01.gif";	
		document.getElementById("SCGMainImageMenu"+strCurrentMenuId+"R").src = "/js/images/sp_menu02.gif";
	}
}




