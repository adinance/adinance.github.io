function NavMenu_mover(theObj,objImageId)
{
	theObj.style.cursor = 'hand';
	if (document.getElementById(objImageId)) { document.getElementById(objImageId).src = "/js/images/arrow01_a.gif"; }
	clearTimeout(timerID);
	theObj.className = "text_Sub_Menu_Top_Over";
}

function NavMenu_mout(theObj,objImageId)
{
	theObj.style.cursor = 'pointer';
	if (document.getElementById(objImageId)) { document.getElementById(objImageId).src = "/js/images/arrow01.gif"; }		
	timerID = setTimeout("NavMenu_destroy()",600);
	theObj.className = "text_Sub_Menu_Top";
}





